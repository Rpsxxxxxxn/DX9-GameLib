#pragma once

//=============================================================
// インクルード
//=============================================================
#include "../../Common.h"

//=============================================================
// クラス定義
//=============================================================
class CSprite
{
public:

	//-------------------------------------------------------------
	// シングルトン生成
	//-------------------------------------------------------------
	static CSprite* Create();

	//-------------------------------------------------------------
	// 初期化
	// 引　数：LPDIRECT3DDEVICE9	デバイスのポインタ
	//-------------------------------------------------------------
	void Initialize(LPDIRECT3DDEVICE9 pDevice);

	//-------------------------------------------------------------
	// 描画
	// 引　数：RECT*				ソース画像のマスク
	// 　　　　LPDIRECT3DTEXTURE9	テクスチャのポインタ
	// 　　　　RECT*				描画先の位置
	// 　　　　DWORD				透明度
	//-------------------------------------------------------------
	void Draw(
		RECT* pSrcRect,
		LPDIRECT3DTEXTURE9 pTexture,
		RECT* pDstRect, 
		DWORD dwColor = 255,
		float angle = 0.0f);

	//-------------------------------------------------------------
	// スプライトレンダリング開始
	// 備　考：描画前に呼び出しを必ず
	//-------------------------------------------------------------
	void SpriteBegin();

	//-------------------------------------------------------------
	// スプライトレンダリング終了
	// 備　考：描画後に呼び出しを必ず
	//-------------------------------------------------------------
	void SpriteClose();

	//-------------------------------------------------------------
	// スプライトのポインタ取得
	//-------------------------------------------------------------
	LPD3DXSPRITE GetSprite();
private:

	//-------------------------------------------------------------
	// コンストラクタ
	//-------------------------------------------------------------
	CSprite();

	//-------------------------------------------------------------
	// デストラクタ
	//-------------------------------------------------------------
	~CSprite();


	//スプライト
	LPD3DXSPRITE m_pSprite;
};