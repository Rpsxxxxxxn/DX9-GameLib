#include "GameTimer.hpp"

using namespace FileConst;

//================================================================================
// クラス定義
//================================================================================

//--------------------------------------------------------------------------------
// コンストラクタ
//--------------------------------------------------------------------------------
CGameTimer::CGameTimer()
	: m_timer(0.0)
{
}

//--------------------------------------------------------------------------------
// デストラクタ
//--------------------------------------------------------------------------------
CGameTimer::~CGameTimer()
{
}

//--------------------------------------------------------------------------------
// 更新
// 引　数：CSystem*	システムクラスのポインタ
//--------------------------------------------------------------------------------
void CGameTimer::Update(CSystem * pSystem)
{
	if (!m_isSleep)
	{
		m_timer += pSystem->GetDeltaTime();
	}
}