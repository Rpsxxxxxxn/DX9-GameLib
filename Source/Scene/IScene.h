#include "GameManager/GameManager.h"
#include "SceneManager.h"

//=============================================================
// クラス定義
//=============================================================
class IScene
{
public:
	//-------------------------------------------------------------
	// コンストラクタ
	//-------------------------------------------------------------
	IScene(CSystem* pSystem)
		: m_pSystem(pSystem) {};

	//-------------------------------------------------------------
	// デストラクタ
	//-------------------------------------------------------------
	virtual ~IScene() {};

	//-------------------------------------------------------------
	// ファイル読み込み
	//-------------------------------------------------------------
	virtual void LoadFile() = 0;

	//-------------------------------------------------------------
	// 初期化
	//-------------------------------------------------------------
	virtual void Initialize() = 0;

	//-------------------------------------------------------------
	// 更新
	//-------------------------------------------------------------
	virtual void Update() = 0;

	//-------------------------------------------------------------
	// シャドウ用描画
	//-------------------------------------------------------------
	virtual void ZDraw() = 0;

	//-------------------------------------------------------------
	// 描画
	//-------------------------------------------------------------
	virtual void Draw() = 0;

	//-------------------------------------------------------------
	// 解放
	//-------------------------------------------------------------
	virtual void Release() = 0;

protected:
	CSystem* m_pSystem;
};